# محاسبه داخل {}
a = 5
b = 3
print(f"{a} + {b} = {a + b}")
# خروجی: 5 + 3 = 8

# فراخوانی تابع داخل {}
name = "babak"
print(f"اسم با حروف بزرگ: {name.upper()}")
# خروجی: اسم با حروف بزرگ: BABAK

# فرمت کردن اعداد
price = 1234567
print(f"قیمت: {price:,} تومان")
# خروجی: قیمت: 1,234,567 تومان

# اعشار محدود
pi = 3.14159265359
print(f"عدد پی: {pi:.2f}")
# خروجی: عدد پی: 3.14

# درصد
score = 0.856
print(f"نمره: {score:.1%}")
# خروجی: نمره: 85.6%